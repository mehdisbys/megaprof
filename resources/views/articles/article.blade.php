<article>
    <div class="container">
        <div class="row">
            <div class="col-lg-8 col-lg-offset-2 col-md-10 col-md-offset-1">

                <img class="img-responsive" src="/images/articles/anglais/image2.jpg" alt="">
                <span class="caption text-muted">Big Ben - Londres</span>


                <p class="article-paragraph">Comment apprendre l’anglais facilement, rapidement et surtout
                avec des résultats ? La réponse dans cet article en 10 points clés!
                Mais avant cela, pourquoi faut-il apprendre l’anglais ? Ce n’est pas un scoop, l’anglais est la langue
                internationale, la langue de l’ouverture sur le monde et la langue des affaires. Et puis saviez-vous que
                les pages en anglais représentent plus de 55% du web mondial ? Alors si vous aviez ou non une raison de
                vous y mettre, apprendre cette langue peut vraiment changer votre vie. Vous aurez accès à des millions
                de pages d’informations sur le web. Vous découvrirez également une nouvelle façon de penser, riche en
                expériences !</p>

               <p class="article-paragraph">Avant de commencer, les 10 points que nous vous proposons nécessitent de faire un grand plongeon, de
                s’investir régulièrement pour apprendre de manière efficace.</p>

               <p class="article-paragraph"> C’est parti ! </p>

                <h2 class="section-heading">1. Pensez en anglais </h2>

               <p class="article-paragraph"> Se mettre complètement dans le bain est également une première clé pour apprendre et progresser en
                anglais. Essayer de penser et de vous parler en anglais partout au quotidien : à la maison, au boulot,
                en marchant, en faisant vos courses, en faisant du sport, avec vos amis etc… Un autre exemple également,
                nous passons énormément de temps au téléphone donc mettez votre smartphone en anglais. Faites de même
                avec votre ordinateur. Au début c’est difficile, vos mots et vos phrases seront très simples mais petit
                à petit à force d’assimiler l’anglais comme votre langue, votre niveau évoluera. </p>

                <h2 class="section-heading">2. Se focaliser sur ces 4 axes clés : Lire, écrire, parler et écouter</h2>

               <p class="article-paragraph"> Ces 4 compétences sont des piliers pour votre apprentissage. Rappelez-vous quand vous étiez en cours,
                très souvent vous aviez un texte à lire avec prise du vocabulaire suivi d’une expression à l’oral et
                d’un enregistrement. Pendant les examens d’anglais comme le TOEFL c’est également le cas. Il y a une
                épreuve de compréhension orale, des questions sur plusieurs textes et une rédaction à faire.
                La clé est de passer d’une compétence à une autre de manière régulière et surtout à petite dose. </p>

                <h2 class="section-heading">3. De la grammaire ? </h2>

              <p class="article-paragraph">  C’est la partie de l’apprentissage qui paraît la plus dur, la plus ennuyeuse mais elle est indispensable
                surtout lorsqu’on pense aux verbes irréguliers par exemple mais il faut les affronter. C’est ce qui
                structurera vos phrases et votre façon de parler. Particulièrement dans le monde professionnel c’est
                nécessaire. Par contre il ne faut pas trop en faire c’est la première erreur à éviter. Les personnes ont
                tendance à commencer au début à trop en faire ce qui joue sur leur motivation. Essayez d’aborder la
                grammaire tout en vous amusant. La méthode la plus naturelle pour apprendre la grammaire est à travers
                la prise de parole. Le but au tout début n’est pas de tout apprendre de manière exhaustive mais d’avoir
                les bases pour continuer à avancer toujours à votre rythme. </p>

                <img class="img-responsive" src="/images/articles/anglais/image1.jpg" alt="">
                <span class="caption text-muted"> Vous avez le choix! </span>

                <h2 class="section-heading">4. Prendre note des nouveaux mots de vocabulaire</h2>

              <p class="article-paragraph">  C’est efficace! Même si le mot nous plait nous ne pouvons pas retenir chaque mot du premier coup. Nous
                vous conseillons donc de les noter dans un cahier en prenant en compte le contexte dans laquelle vous
                l’avez utilisé. Cela vous permettra de gagner du temps sans avoir à chercher le pourquoi de ces mots.
                Ensuite lisez et apprenez les chaque jour pendant 5 minutes. </p>


                <h2 class="section-heading">5. Imprégnez-vous des expressions anglaises les plus courantes</h2>

               <p class="article-paragraph"> C’est fondamental. Un bon niveau scolaire ne vous suffira pas pour comprendre l’anglais parlé. Les
                expressions courantes vous permettront de vous faire comprendre d’un natif. Elles rendront également
                votre anglais fluide. Il vous faut vous familiariser avec elles afin de pouvoir les utiliser.
                En voici quelques unes :
                A blessing in disguise (Une bonne chose qui semblait mauvaise)
                A dime a dozen (commun, courant)
                Beat around the bush (ne pas dire une chose franchement parce que c’est dur)
                Better late than never (Mieux tard que jamais)
                Bite the bullet (faire une chose désagréable mais nécessaire)
                Break a leg (bonne chance) </p>

                <h2 class="section-heading">6. Prendre des cours</h2>

               <p class="article-paragraph"> Nous vous rassurons, vous pouvez réussir sans prendre de cours. Cependant cela peut vous aider à vous
                structurer, à vous évaluer, vous fera avancer plus vite. Si possible, nous vous conseillons donc de
                prendre un cours particulier d’anglais. Vous en trouverez partout au Maroc, dans les grandes villes
                comme Casablanca, Tanger, Rabat etc… Vous trouverez plus de détail dans l’article « Soutien scolaire :
                Faites des économies » pour vous aider à faire le choix d’un cours à domicile au meilleur prix.
               </p>

                <h2 class="section-heading">7. Rencontrez des anglophones</h2>

               <p class="article-paragraph"> C’est vrai, l’immersion dans un pays anglophones est la meilleure solution pour rencontrer des
                anglophones. Vous apprendrez et progresserez très rapidement en anglais. Vous apprendrez un anglais,
                celui de la vie quotidienne de tous les jours. Ce sera également l’occasion de réfléchir dans cette
                langue. Mais c’est vrai aussi que c’est une solution chère et qu’il est tout à fait possible de
                rencontrer des anglophones sans partir dans un pays anglophone et de progresser également. Vous pouvez
                chercher à faire un échange linguistique par exemple dans un endroit choisi, vous parlerez 1H de
                marocain contre 1H d’anglais. Vous pouvez également rencontrer les étudiants étrangers. Si vous en
                trouvez pas, cherchez une personne qui a envie de parler en anglais et motivez-vous l’un l’autre. </p>

                <h2 class="section-heading">8. Vos passions et vos centres d’intérêts en anglais</h2>

              <p class="article-paragraph">  L’une des clés de réussite dans l’apprentissage de l’anglais est aussi dans le fait de pratiquer vos
                passions et vos centres d’intérêts totalement en anglais. Quel que soit vos centres d’intérêts, c’est
                toujours plus facile d’apprendre quand on aime ce que l’on fait. Regarder vos programmes TV préférées en
                anglais, regarder vos séries favorites en anglais, écouter vos musiques en anglais et de manière
                générale exercez vos activités le plus possible en anglais. Vivez l’anglais ! </p>


                <img class="img-responsive" src="/images/articles/anglais/image3.jpg" alt="">
                <span class="caption text-muted"> Série américaine : Criminal Minds</span>



                <h2 class="section-heading">9. Regarder des séries en anglais</h2>

                C’est la partie plaisir. Et oui vous pouvez apprendre l’anglais de manière efficace en regardant des
                séries et choisissez celle qui vous plaît ! Vous assimilez l’anglais de tous les jours, des expressions
                de la vie courante naturellement et sans effort. Oui c’est le moment de pause ! Pour débuter, pendant 1
                à 2 semaines vous pouvez regarder une série avec le son anglais et avec les sous-titre en marocain ou en
                français. Essayez de vous concentrer sur la voix des acteurs pour vous habituer à distinguer les
                différents sons. Vous apprendrez énormément de vocabulaires. Ensuite vous regarderez votre série
                préférée pendant 3 à 4 semaines en son anglais et avec les sous-titres en anglais. Alors certes vous ne
                comprendrez pas tout mais vous ferez beaucoup de progrès. Une véritable ambiance 100% anglaise ! Puis
                après ces 3 à 4 semaines, n’utilisez plus les sous-titres, vous serez complètement autonome et cela vous
                incitera à faire plus d’effort. Pour information, vous avez aussi la TV anglaise comme la BBC ou
                filmon.com par exemple. Une source supplémentaire pour vous.


                <h2 class="section-heading">10. Lisez des livres, écoutez des podcasts</h2>

              <p class="article-paragraph">  Ou même des bandes dessinées. Oui même les bandes dessinées, les images permettent de faciliter la
                compréhension. La lecture vous permettra d’avoir une base solide en anglais. Elle enrichit le
                vocabulaire et renforce la maîtrise de la langue. Elle est donc indispensable si vous voulez faire des
                progrès dans le temps et durable. Un petit conseil que nous vous donnons, ne lisez pas pour lire, lisez
                ce qui vous intéresse et il n’y a pas de petit lecture : Romans, fiction, politique, philosophie, la
                Presse, les BD, magazine d’actualité, magazine sportifs... </p>


               <p class="article-paragraph"> Il y a les podcasts, incontournables pour écouter de l’anglais tous les jours. Il en existe tellement,
                sur tous les sujets que vous voulez et la plupart sont gratuits. Faites une recherche en fonction de ce
                que vous aimez et abonnez-vous ! Pour exemple, les podcasts de la BBC sont très intéressants et
                formateurs. Ils correspondent au vrai anglais parler en Angleterre. Vous avez également « The English we
                speak », « Voice of America » et « English as a Second language ». Vous pouvez écouter les podcasts le
                matin pendant votre trajet, pendant le déjeuner, pendant vos activités ou le soir avant de dormir. </p>

              <p class="article-paragraph"> En résumé, ne pas avoir un niveau minimum d’anglais est un handicap tant sur le plan professionnel que
                sur le plan des échanges avec le monde. Donc Après ces 10 conseils, vous n’avez plus d’excuses pour
                apprendre ou devenir meilleur en anglais ! Vous apprenez l’anglais pour vous, prenez du plaisir en le
                faisant ! </p>

